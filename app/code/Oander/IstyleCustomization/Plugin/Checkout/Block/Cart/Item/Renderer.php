namespace Oander\IstyleCustomization\Plugin\Checkout\Block\Cart\Item;

/**
 * Class Renderer
 * @package Oander\IstyleCustomization\Plugin\Checkout\Block\Cart\Item
 */
class Renderer
{
    /**
     * @var \Magento\Catalog\Api\Data\ProductInterface
     */
    protected $productInterface;

    /**
     * Renderer constructor.
     * @param \Magento\Catalog\Api\Data\ProductInterface $productInterface
     */
    public function __construct(
        \Magento\Catalog\Api\Data\ProductInterface $productInterface
    ) {
        $this->productInterface = $productInterface;
    }

    /**
     * @param \Magento\Checkout\Block\Cart\Item\Renderer $subject
     * @param string $productName
     * @return mixed
     */
    public function afterGetProductName(
        \Magento\Checkout\Block\Cart\Item\Renderer $subject,
        string $productName
    )
    {
        if(!$productName) {
            return $productName;
        }

        $simpleProduct = $this->productInterface->loadByAttribute('sku', $subject->getProduct()->getSku());

        if ($simpleProduct) {
            $productName = $simpleProduct->getName();
        }

        return $productName;
    }

}